    private void bntFecharContaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntFecharContaActionPerformed
        double totalVendido = Double.parseDouble(txtTotalVendas.getText());
        double desconto = Integer.parseInt(txtDesconto.getText());
        double valorRecebido = Integer.parseInt(txtValorRecebido.getText());
        
        if (cmbTipoPagamento.getSelectedItem().toString().equalsIgnoreCase("A Vista (Desconto)")) {
            double total = (totalVendido - (totalVendido * (desconto / 100)));
            txtTotal.setText("Total: " + total);
            Facilitador(total);
            
        } else if (cmbTipoPagamento.getSelectedItem().toString().equalsIgnoreCase("Cartao no Credito (Acrescimo)")) {
            double total = (totalVendido + (totalVendido * (desconto / 100)));
            txtTotal.setText("Total: " + total);
            Facilitador(total);
            
        } else {
            double total = (totalVendido + (totalVendido * (desconto / 100)));
            txtTotal.setText("Total: " + total);
            Facilitador(total);
        }

        double troco = valorRecebido - totalVendido;

        txtTroco.setText("Troco: " + troco);

    }//GEN-LAST:event_bntFecharContaActionPerformed

