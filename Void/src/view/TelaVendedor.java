    private void refreshVendasTable() {
        VendasRep lis = new VendasRep();
        vend = new VendasInterface(lis.listar());
        tblListaVendas.setModel(vend);
    }

    private void refreshProdutoTable() {
        ProdutoRep t = new ProdutoRep();
        prod = new ProdutoInterface(t.listar());
        tblProdutosBanco.setModel(prod);
    }

    public void pegarLinha() {
        int linha = tblListaVendas.getSelectedRow();

        if (linha != -1) {
            String quantidade = tblListaVendas.getValueAt(linha, 3).toString();

            String preco = tblListaVendas.getValueAt(linha, 4).toString();

            double total = Integer.parseInt(quantidade) * Double.parseDouble(preco);
            
            txtTotalVendas.setText(String.valueOf(Math.round(total)));
        }
    }

    public void Facilitador(double total) {
        
        int linha = tblListaVendas.getSelectedRow();
        String id = tblListaVendas.getValueAt(linha, 0).toString();
        String cod = tblListaVendas.getValueAt(linha, 1).toString();
        String nome = tblListaVendas.getValueAt(linha, 2).toString();
        String qtd = tblListaVendas.getValueAt(linha, 3).toString();
        String preco = tblListaVendas.getValueAt(linha, 4).toString();
        String nomeCliente = tblListaVendas.getValueAt(linha, 5).toString();

        Relatorio relatorio = new Relatorio();
        RelatorioRep rrep = new RelatorioRep();
        VendasRep vrep = new VendasRep();
        Vendas vendas = new Vendas();
        
        vendas.setidvendas(Integer.parseInt(id));
        
        relatorio.setTotalVendas(total);
        relatorio.setCodProduto(Integer.valueOf(cod));
        relatorio.setNomeProduto(nome);
        relatorio.setQtdProduto(Integer.valueOf(qtd));
        relatorio.setPrecoProduto(BigDecimal.valueOf(Double.valueOf(preco)));
        relatorio.setNomeCliente(nomeCliente);
        rrep.adicionar(relatorio);
        vrep.excluir(vendas);
        refreshVendasTable();
    }

