    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed

        usuarioRep uRep = new usuarioRep();
        Usuario usu = uRep.checarLogin(txtLogin.getText(), txtSenha.getText());
               
        
        if (usu.getIdusuario() != null) {
            if (usu.getCargo().equalsIgnoreCase("vendedor")) {
                new TelaVendedor().setVisible(true);
                dispose();
            } else if (usu.getCargo().equalsIgnoreCase("estoquista")) {
                new TelaEstoquista().setVisible(true);
                dispose();
            }else if (usu.getCargo().equalsIgnoreCase("gerente")) {
                new TelaGerente().setVisible(true);
                dispose();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Credenciais inv√°lidas!");
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

